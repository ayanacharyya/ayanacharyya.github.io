<p>Hola participant or interested student!</p>

<p>Welcome to the Mt Stromlo Student Seminar website. Last year's seminars were a huge success and we hope to welcome you back this year. </p> 

<p>The 2 day event is designed to be a fun conference for astronomy and astrophysics research students from all over Australia (and overseas). Taking part in the seminars gives you the chance to network with your peers (and future colleagues), practice those all important public speaking skills and learn about a variety of fields in astronomy. </p> 

<h2>Logistics</h2>
<p>The seminars will be held on the 1st and 2nd of December at Mt Stromlo Observatory in Canberra, Australia. The seminars are fully catered. Lunch and refreshments will be provided on both days as well as dinner on the 1st. </p>


<!-- <h2> Seminars Outline</h2> -->
<p> This year's theme is the 'Hitchhikers Guide to the Galaxy'. </p><!--(Why? Because we think <i> there's no place like space!</i>).-->

<p> The first day of the seminars is dedicated to invited talks from distinguished astronomers, workshops and a tour of the Giant Magellan Telescope facilities.  Student participants will have a chance to present their work on the second day. </p> 
<p> The time limit for the student talks is 12 minutes (10 minutes for your talk and 2 minutes for questions). You are encouraged to share your exciting research, something funny, or something you find relevant to the theme. Better yet, you can incoporate the theme into your science talk.  Based on the participants' votes there will be prizes for: 1) the best science talk,  2) the best theme-related talk and 3) the best overall talk. 
Lucrative prizes await, so bring your a-game!  </p>


