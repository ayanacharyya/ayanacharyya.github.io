<p> The <a href="http://www.gmto.org/overview/"> <font color= "red"><b>Giant Magellan Telescope (GMT) </b></font> </a> </b> is a next generation super-giant, segmented mirror telescope that will revolutionise astronomy. Six off-axis segments surround the central mirror, giving a total collecting area of 368 square metres. Equipped with adaptive optics, the GMT is expected to have resolving power 10 times greater than the Hubble Space Telescope.  </p>

<p>The Research School of Astronomy and Astrophysics (RSAA) has a share in the GMT consortium and contributes to the telescope design, as well as instrumentation research and development.</p>

